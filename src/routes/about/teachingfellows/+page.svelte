        <h3>Laboratory Teaching Fellows</h3>
        <p>
            Laboratory Teaching Fellows (LTFs) are responsible for assisting
            ENES100 instructors, overseeing the labs, and assisting with
            instruction of content such as Arduino programming. The average time
            commitment for an LTF is 12 hours a week for 16 weeks.
        </p>
        <p>Job duties may include:</p>
        <ul>
            <li>
                Assist with the teaching of course-related software tools (e.g.
                Arduino programming)
            </li>
            <li>
                Be trained in and safely operate a variety of shop tools to
                include: power drills, powered saws, powered sanders, soldering
                irons, 3D printers, and laser cutters
            </li>
            <li>
                Assist with the preparation and running of in-class activities
                that use lab materials and tools
            </li>
            <li>
                Assist with the overall supervision, maintenance, and
                improvement of the Keystone Laboratories
            </li>
            <li>
                Assist with the development of project-specific hardware used by
                all students (e.g. arena)
            </li>
            <li>
                Provide support to student teams as they prototype and
                troubleshoot their designs
            </li>
            <li>Attend and participate in weekly ENES100 TF meetings</li>
            <li>
                Other responsibilities as assigned by the ENES100 instructors
            </li>
        </ul>
        <p>Qualifications:</p>
        <ul>
            <li>
                Good academic standing with an overall G.P.A. > 3.0 (> 3.00
                preferred)
            </li>
            <li>
                Successful completion of ENES100 with a letter grade of A- or
                higher
            </li>
            <li>Excellent oral and interpersonal communication skills</li>
            <li>
                Experience with computer programming and electronic circuits,
                and experience with mechanical design and fabrication methods
            </li>
            <li>
                Sophomores through seniors are considered, with preference for
                those who will not be graduating within the year they serve as
                an LTF
            </li>
            <li>Electrical and Computer engineering major is a plus!</li>
            <li>
                Commitment to attend ALL in-person training sessions and the
                weekly ENES100 TF meetings
            </li>
        </ul>
        <p>Training:</p>
        <ul>
            <li>
                In-person ENES100 TF training will take place the week before
                the semester begins, exact dates and times TBA. Attendance at
                the in-person ENES100 TF training is required for ALL ENES100
                LTFs.
            </li>
            <li>
                All Keystone TFs must have a certificate of completion for the <a
                    href="https://umd-registrar.catalog.instructure.com/courses/umd-ferpa-tutorial"
                    >UMD FERPA Tutorial</a
                > on file with the Keystone Program.
            </li>
        </ul>
        <p>Benefits:</p>
        <ul>
            <li>$2,900 per semester stipend</li>
            <li>
                Access to the Teaching Fellows office in the J. M. Patterson
                Building
            </li>
            <li>
                Opportunity to reinforce knowledge of engineering fundamentals
                by tutoring
            </li>
            <li>
                Opportunity to build your resume in "professional engineering
                skills" such as coaching, teaching, conflict management, project
                management, and leadership
            </li>
            <li>
                Opportunity to work with faculty members who may serve as
                references for employment or graduate school
            </li>
        </ul>
        <p>
            <b
                >The position will be adjusted throughout the semester to meet
                the needs of the class.</b
            >
        </p>
        <p>
            Expect to hold ~8 hours in the labs per week preparing the lab
            spaces for use and supporting instructors (Monday - Thursday during
            scheduled ENES100 class times), and ~4 hours in the labs on Fridays.
            LTFs are not expected to be able to participate in every section of
            the course, but their time needs to be scheduled weekly so that they
            can assist with the same sections each week in terms of overseeing
            the labs, providing demonstrations, and assisting with the
            instruction of content such as Arduino programming.
        </p>
