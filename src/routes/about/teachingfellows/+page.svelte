        <h3>Undergraduate Teaching Fellows</h3>
        <p>
            ENES100 Undergraduate Teaching Fellows (UTFs) are assigned to work
            with one professor in one section of ENES100 (~40 students). The
            average time commitment for an ENES100 UTF is 10 hours per week for
            15 weeks.
        </p>
        <p>Job duties may include:</p>
        <ul>
            <li>
                Attend ALL 4 hours of ENES100 class lectures and lab
            </li>
            <li>
                Remain current on content being taught each week
            </li>
            <li>
                Meet regularly with your assigned ENES100 section instructor
            </li>
            <li>
                Attend and participate in weekly ENES100 TF meetings
            </li>
            <li>
                Maintain 3 weekly office hours in the Keystone Center and during open lab sessions, meeting with students and/or teams as needed
            </li>
            <li>
                Assist the ENES100 instructor in the development of instructional and assessment materials
            </li>
            <li>Assist in administering and grading homework assignments, group work, quizzes, and/or other forms of student assessment</li>
            <li>
                Other responsibilities as assigned by the ENES100 instructorsMaintain records of student attendance and progress
            </li>
            <li>Under the supervision of the instructor, plan and teach one or two class periods during the semester</li>
            <li>Assist with staffing of open lab hours during the prototype fabrication segment of the course</li>
            <li>Serve as a resource to the sectionâ€™s teams, providing feedback as requested and facilitating team interactions</li>
            <li>Provide information about and encourage involvement in engineering student societies and activities</li>
            <li>Provide referrals for assistance such as academic advising, study skills, time management, general tutoring, and personal counseling</li>
            <li>Other responsibilities as assigned by the instructor</li>
        </ul>
        <p>Qualifications:</p>
        <ul>
            <li>
                Sophomore, Junior, or Senior
            </li>
            <li>
                Engineering major student
            </li>
            <li>Good academic standing with an overall G.P.A. > 3.0  (> 3.00 preferred)</li>
            <li>
                Computer skills: Microsoft Word, Excel, and PowerPoint (or Google equivalent) required
            </li>
            <li>
                Good oral and written communication skills and good interpersonal skills
            </li>
            <li>Familiarity with Clark School and University resources (i.e., Engineering Co-op & Career Services office, academic advising, UMD and Clark School computer facilities, Keystone Center)</li>
            <li>
                Commitment to attend ALL 4 hours of the assigned ENES100 section per week, ALL in-person training sessions, and the weekly ENES100 TF meetings
            </li>
        </ul>
        <p>Training:</p>
        <ul>
            <li>
                In-person ENES100 TF training will take place the week before
                the semester begins, exact dates and times TBA. Attendance at
                the in-person ENES100 TF training is required for ALL ENES100
                UTFs.
            </li>
            <li>An online teaching fellow training program will take place prior to and for the first few weeks of the semester. Completion of this training program is required for all new Keystone TFs.</li>
            <li>
                All Keystone TFs must have a certificate of completion for the <a
                    href="https://umd-registrar.catalog.instructure.com/courses/umd-ferpa-tutorial"
                    >UMD FERPA Tutorial</a
                > on file with the Keystone Program.
            </li>
        </ul>
        <p>Benefits:</p>
        <ul>
            <li>$2,400 per semester stipend</li>
            <li>
                Access to the Teaching Fellows office in the J. M. Patterson
                Building
            </li>
            <li>
                Opportunity to reinforce knowledge of engineering fundamentals
                by tutoring
            </li>
            <li>
                Opportunity to build your resume in "professional engineering
                skills" such as coaching, teaching, conflict management, and project
                management
            </li>
            <li>
                Opportunity to work with faculty members who may serve as
                references for employment or graduate school
            </li>
        </ul>
